let x = 20;
let y = 3;

console.log(x+y);   // Addition
console.log(x-y);   // Subtraction
console.log(x*y);   // Multiplication
console.log(x/y);   // Division
console.log(x%y);   // Remainder
console.log(x**y);  // Exponent

// Increment
let a = 10;
console.log(a++);
console.log(a);

// Decrement
let b = 4;
console.log(--b);
console.log(b);


// Assignment
let i = 5;
i += 5;
console.log(i);