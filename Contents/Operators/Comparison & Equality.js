
let x=1;

// Relational
console.log(x > 0);
console.log(x >= 1);
console.log(x < 1);
console.log(x <= 1);


// Strict Equality
console.log("Strict Equality");
console.log(1 === 1);
console.log('1' !== 1);

// Loose Equality
console.log("Loose Equality");
console.log(1 == 1);
console.log('1' == 1);
console.log(true == 1);

// Ternary
console.log(3 > 2 ? true : false);